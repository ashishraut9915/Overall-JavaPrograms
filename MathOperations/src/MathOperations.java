
public class MathOperations {

	static int add(int num1,int num2) {
		
		return num1+num2;
	}
	
	
	
	
	
	public static void main(String[] args) {
		
		int add1 =	MathOperations.add(5,7);
		int add2 =  MathOperations.add(10,20);
		System.out.println("Adding 5 and 7 :"+add1);
		System.out.println("Adding 10 and 20 :"+add2); 
		
	}
	
}
